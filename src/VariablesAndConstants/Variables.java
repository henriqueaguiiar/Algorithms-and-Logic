package VariablesAndConstants;

public class Variables {
    public static void main(String[] args) {

        // -- Variaveis

        String txt = "Henrique"; // Declaração de variavel com um valor String definido
        System.out.println(txt);


        txt = "Daniel";  // Sobrescrita de variavel
        System.out.println(txt);

        // -----------------------------------------

        // -- Constantes

        final String txt2 = "Luciana"; // declaração de constante 'final' com valor definido
        System.out.println(txt2);

        // a palavra 'final' no inicio da constante, define a constante impedindo sobrescrita do valor da constante.


    }
}

//Variavel = Valores variaveis, podem ser alterados.
//Constante = Valores Fixos uma vez definidos não pode se alterar.

//Sintaxe:  <Tipo>  <Nome> = <Valor>
